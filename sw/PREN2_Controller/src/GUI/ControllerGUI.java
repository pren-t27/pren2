    private Mat turnMat(Mat inputMat) {
        Point src_center = new Point(inputMat.cols() / 2, inputMat.rows() / 2);
        Mat rot_mat = getRotationMatrix2D(src_center, 180, 1.0);
        Mat dst = new Mat();
        warpAffine(inputMat, dst, rot_mat, inputMat.size());

        return dst;
    }

