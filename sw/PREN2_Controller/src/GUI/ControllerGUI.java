    public BufferedImage encodeImage(Mat pImage) {
        MatOfByte mem = new MatOfByte();
        try {
            imencode(".bmp", pImage, mem);
            Image im = ImageIO.read(new ByteArrayInputStream(mem.toArray()));
            return ((BufferedImage) im);
        } catch (Exception ex) {
            ex.getMessage();
            return null;
        }
    }

